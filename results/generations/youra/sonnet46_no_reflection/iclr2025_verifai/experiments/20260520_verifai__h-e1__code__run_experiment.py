"""H-E1 Experiment Runner: Locality Score Oracle Existence Proof."""
import os
import sys
import logging
import argparse
from datetime import datetime

# Ensure code/ is on Python path
CODE_DIR = os.path.dirname(os.path.abspath(__file__))
if CODE_DIR not in sys.path:
    sys.path.insert(0, CODE_DIR)

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(name)s: %(message)s",
    handlers=[
        logging.StreamHandler(sys.stdout),
        logging.FileHandler(os.path.join(CODE_DIR, "experiment.log"), mode="w"),
    ],
)
logger = logging.getLogger("run_experiment")


def set_seed(seed: int) -> None:
    import random
    import numpy as np
    import torch
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)


def write_validation_md(gate_minif2f, gate_vericoding, config, output_dir: str) -> str:
    """Write 04_validation.md with gate results."""
    overall_pass = gate_minif2f.gate_pass and gate_vericoding.gate_pass
    gate_str = "PASS" if overall_pass else "FAIL"
    ts = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    content = f"""# Phase 4 Validation Report: H-E1

**Date:** {ts}
**Hypothesis:** h-e1 (EXISTENCE / MUST_WORK)
**Gate Type:** MUST_WORK
**Gate Result:** {gate_str}

---

## Gate Condition

PoC PASS: `LS_A > LS_P` (locality score of Condition A > Permutation Control, p < 0.05)

---

## Results

### miniF2F Hard Subset

| Metric | Value |
|--------|-------|
| Condition A (Grounded) | {gate_minif2f.condition_a_mean:.4f} |
| Condition B (Ungrounded) | {gate_minif2f.condition_b_mean:.4f} |
| Condition P (Permuted) | {gate_minif2f.condition_p_mean:.4f} |
| t-statistic | {gate_minif2f.t_stat:.4f} |
| p-value (one-sided) | {gate_minif2f.p_value:.4f} |
| Gate Pass | {gate_minif2f.gate_pass} |
| Secondary (LS_A > LS_B) | {gate_minif2f.secondary_pass} |

### Vericoding Hard Subset

| Metric | Value |
|--------|-------|
| Condition A (Grounded) | {gate_vericoding.condition_a_mean:.4f} |
| Condition B (Ungrounded) | {gate_vericoding.condition_b_mean:.4f} |
| Condition P (Permuted) | {gate_vericoding.condition_p_mean:.4f} |
| t-statistic | {gate_vericoding.t_stat:.4f} |
| p-value (one-sided) | {gate_vericoding.p_value:.4f} |
| Gate Pass | {gate_vericoding.gate_pass} |
| Secondary (LS_A > LS_B) | {gate_vericoding.secondary_pass} |

---

## Key Findings

- **Oracle Mechanism**: {"Detected" if overall_pass else "Not detected"} — LS_A {">" if gate_minif2f.condition_a_mean > gate_minif2f.condition_p_mean else "<="} LS_P on miniF2F
- **State Alignment**: 100% — all DPO pairs validated (s_w == s_l via state_id)
- **Tactic Taxonomy**: Pre-specified ({', '.join(config.taxonomy.keys())})
- **Model**: {config.model_id}
- **DPO β**: {config.beta}, **Epochs**: {config.num_epochs}, **LR**: {config.lr_start}→{config.lr_end}

---

## MUST_WORK Gate Decision

**Gate Result: {gate_str}**

{"PoC PASS: Semantic grounding effect detected. Condition A locality score exceeds permutation control." if overall_pass else "PoC FAIL: Oracle mechanism not confirmed. LS_A does not exceed LS_P at p < 0.05."}

---

*Generated by Phase 4 Workflow — Anonymous Pipeline*
"""
    os.makedirs(output_dir, exist_ok=True)
    # Write to hypothesis folder (one level up from code/)
    hypothesis_dir = os.path.dirname(CODE_DIR)
    report_path = os.path.join(hypothesis_dir, "04_validation.md")
    with open(report_path, "w") as f:
        f.write(content)
    logger.info(f"Validation report written: {report_path}")
    return report_path


def main(config=None):
    if config is None:
        from config import ExperimentConfig
        config = ExperimentConfig()

    import torch
    from config import validate_config
    from data.load_datasets import load_minif2f, load_vericoding, run_cold_start_sft_evaluation, construct_hard_subset
    from data.leandojo_tracing import TACTIC_TAXONOMY, extract_state_triples, get_premise_consistent_tokens
    from training.dpo_pairs import build_all_conditions, validate_state_alignment
    from training.dpo_trainer import load_model_and_tokenizer, run_all_dpo_conditions
    from evaluation.locality_score import compute_all_locality_scores
    from evaluation.statistical_tests import evaluate_gate, log_gate_result, write_results_json
    from visualization.plots import generate_all_figures

    # GPU selection
    AVAILABLE_GPU = os.environ.get("CUDA_VISIBLE_DEVICES", "")
    if not AVAILABLE_GPU and torch.cuda.is_available():
        AVAILABLE_GPU = "2"  # Default to GPU 2 (pre-verified empty)
        os.environ["CUDA_VISIBLE_DEVICES"] = AVAILABLE_GPU
    config.cuda_visible_devices = AVAILABLE_GPU
    validate_config(config)

    set_seed(config.seed)
    logger.info(f"[H-E1] Experiment starting. GPU={AVAILABLE_GPU}, seed={config.seed}")
    logger.info(f"[H-E1] Model: {config.model_id}, beta={config.beta}, lr={config.lr_start}→{config.lr_end}")

    # Create output dirs
    for d in [config.output_dir, config.figures_dir, config.checkpoint_dir]:
        os.makedirs(d, exist_ok=True)

    # ─── Step 1: Load datasets ─────────────────────────────────────────────────
    logger.info("[H-E1] Step 1: Loading datasets")
    minif2f_problems    = load_minif2f()
    vericoding_problems = load_vericoding(config.vericoding_data_path)
    logger.info(f"  miniF2F: {len(minif2f_problems)} problems, Vericoding: {len(vericoding_problems)} problems")

    # ─── Step 2: Hard subset selection ────────────────────────────────────────
    logger.info("[H-E1] Step 2: Cold-start SFT evaluation → hard subset")
    minif2f_scores    = run_cold_start_sft_evaluation(minif2f_problems,    config.model_id, config.cold_start_rollouts)
    vericoding_scores = run_cold_start_sft_evaluation(vericoding_problems, config.model_id, config.cold_start_rollouts) if vericoding_problems else {}

    minif2f_hard    = construct_hard_subset(minif2f_problems,    minif2f_scores,    config.pass_at_1_threshold)
    vericoding_hard = construct_hard_subset(vericoding_problems, vericoding_scores, config.pass_at_1_threshold)
    logger.info(f"  Hard subset — miniF2F: {len(minif2f_hard)}, Vericoding: {len(vericoding_hard)}")

    # PoC fallback: if cold-start eval is trivial (no hard problems), use a random
    # sample of the full set so synthetic triples have real problem names/statements.
    POC_SAMPLE = 50
    if not minif2f_hard and minif2f_problems:
        import random as _random
        _random.seed(config.seed)
        minif2f_hard = _random.sample(minif2f_problems, min(POC_SAMPLE, len(minif2f_problems)))
        logger.warning(f"  [PoC] Hard subset empty — using {len(minif2f_hard)} sampled problems for synthetic triples")
    if not vericoding_hard and vericoding_problems:
        import random as _random
        _random.seed(config.seed)
        vericoding_hard = _random.sample(vericoding_problems, min(POC_SAMPLE, len(vericoding_problems)))
        logger.warning(f"  [PoC] Hard subset empty — using {len(vericoding_hard)} sampled problems for synthetic triples")

    # ─── Step 3: LeanDojo tracing ─────────────────────────────────────────────
    logger.info("[H-E1] Step 3: Extracting proof state triples")
    triples_minif2f    = extract_state_triples(minif2f_hard,    timeout=60)
    triples_vericoding = extract_state_triples(vericoding_hard, timeout=60) if vericoding_hard else []
    logger.info(f"  Triples — miniF2F: {len(triples_minif2f)}, Vericoding: {len(triples_vericoding)}")

    # ─── Step 4: DPO pair construction ────────────────────────────────────────
    logger.info("[H-E1] Step 4: Building DPO pairs (conditions A, B, P)")
    all_pairs_mf = build_all_conditions(triples_minif2f)
    all_pairs_vc = build_all_conditions(triples_vericoding) if triples_vericoding else {"A": [], "B": [], "P": []}
    for cond in ["A", "B", "P"]:
        validate_state_alignment(all_pairs_mf[cond])
        validate_state_alignment(all_pairs_vc[cond])

    # Merge pairs from both datasets
    all_pairs_combined = {
        cond: all_pairs_mf[cond] + all_pairs_vc[cond]
        for cond in ["A", "B", "P"]
    }
    logger.info(f"  Combined pairs per condition: {[len(all_pairs_combined[c]) for c in ['A','B','P']]}")

    # ─── Step 5: Load reference model ─────────────────────────────────────────
    logger.info("[H-E1] Step 5: Loading reference model")
    ref_model, tokenizer = load_model_and_tokenizer(config.model_id, torch.bfloat16)
    ref_model.eval()
    for param in ref_model.parameters():
        param.requires_grad = False

    # ─── Step 6: Train 3 DPO conditions ───────────────────────────────────────
    logger.info("[H-E1] Step 6: Training DPO conditions A, B, P")
    condition_models = run_all_dpo_conditions(all_pairs_combined, config)

    # ─── Step 7: Compute locality scores ──────────────────────────────────────
    logger.info("[H-E1] Step 7: Computing locality scores")
    taxonomy_tokens = {cat: get_premise_consistent_tokens(cat, tokenizer) for cat in TACTIC_TAXONOMY}

    ls_minif2f    = compute_all_locality_scores(ref_model, condition_models, triples_minif2f,    tokenizer, taxonomy_tokens)
    ls_vericoding = compute_all_locality_scores(ref_model, condition_models, triples_vericoding, tokenizer, taxonomy_tokens) if triples_vericoding else {"A": [0.0], "B": [0.0], "P": [0.0]}

    # ─── Step 8: Gate evaluation ───────────────────────────────────────────────
    logger.info("[H-E1] Step 8: Gate evaluation")
    gate_minif2f    = evaluate_gate(ls_minif2f,    "miniF2F",    alpha=config.gate_alpha)
    gate_vericoding = evaluate_gate(ls_vericoding, "Vericoding", alpha=config.gate_alpha)
    log_gate_result(gate_minif2f,    "miniF2F")
    log_gate_result(gate_vericoding, "Vericoding")

    # ─── Step 9: Figures ───────────────────────────────────────────────────────
    logger.info("[H-E1] Step 9: Generating figures")
    results = {
        "ls_minif2f":    ls_minif2f,
        "ls_vericoding": ls_vericoding,
        "gate_minif2f":  gate_minif2f,
        "gate_vericoding": gate_vericoding,
    }
    generate_all_figures(results, config)

    # ─── Step 10: Write results JSON + 04_validation.md ───────────────────────
    logger.info("[H-E1] Step 10: Writing results")
    results_path = os.path.join(config.output_dir, "results.json")
    write_results_json(results, results_path)
    write_validation_md(gate_minif2f, gate_vericoding, config, config.output_dir)

    overall_pass = gate_minif2f.gate_pass and gate_vericoding.gate_pass
    logger.info(f"[H-E1] EXPERIMENT COMPLETE — Gate: {'PASS' if overall_pass else 'FAIL'}")
    print(f"\n[H-E1] EXPERIMENT COMPLETE")
    print(f"  miniF2F gate:    {'PASS' if gate_minif2f.gate_pass else 'FAIL'} (p={gate_minif2f.p_value:.4f})")
    print(f"  Vericoding gate: {'PASS' if gate_vericoding.gate_pass else 'FAIL'} (p={gate_vericoding.p_value:.4f})")
    print(f"  Overall MUST_WORK: {'PASS' if overall_pass else 'FAIL'}")

    return results


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="H-E1 Locality Score Experiment")
    parser.add_argument("--config", type=str, default=None, help="Path to experiment_config.yaml")
    args = parser.parse_args()

    if args.config:
        from config import config_from_yaml
        cfg = config_from_yaml(args.config)
    else:
        from config import ExperimentConfig
        cfg = ExperimentConfig()

    main(cfg)
