def build_all_yaml_configs(
    cfg: HM2Config,
    quick: bool = True,
) -> None:
    """Generate gpt-neox YAML config files for all 8 corpus configs (C0-C7)."""
    train_iters = cfg.train_iters_quick if quick else cfg.train_iters_full
    configs_dir = Path(cfg.configs_dir)
    configs_dir.mkdir(parents=True, exist_ok=True)

    for config_id in ALL_CONFIGS:
        data_path = str(Path(cfg.tokenized_dir) / f"config_{config_id}" / "text_document")
        save_dir = str(Path(cfg.checkpoints_dir) / f"config_{config_id}")
        tensorboard_dir = str(Path(cfg.results_dir) / "tensorboard" / f"config_{config_id}")
        yaml_str = build_gptneox_yaml_config(
            config_id=config_id,
            data_path=data_path,
            save_dir=save_dir,
            tensorboard_dir=tensorboard_dir,
            train_iters=train_iters,
        )
        yaml_path = configs_dir / f"pythia-1b-hm2-{config_id}.yml"
        yaml_path.write_text(yaml_str, encoding="utf-8")
        print(f"[data_prep] Written: {yaml_path}")


def tokenize_corpus(
    corpus_path: str,
    output_dir: str,
    tokenizer_name: str = "EleutherAI/pythia-1b",
    seq_length: int = 2048,
    workers: int = 4,
) -> None:
    """Tokenize a JSONL corpus using gpt-neox tools/preprocess_data.py.

    Writes output to output_dir/text_document.{bin,idx}.
    Falls back to HuggingFace tokenizer + custom writer if gpt-neox unavailable.
    """
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    output_prefix = str(output_dir / "text_document")

    # Try gpt-neox preprocess_data.py first
    gptneox_script = Path(__file__).parent.parent.parent / "gpt-neox" / "tools" / "preprocess_data.py"
    if gptneox_script.exists():
        cmd = [
            "python", str(gptneox_script),
            "--input", corpus_path,
            "--output-prefix", output_prefix,
            "--tokenizer-type", "HFTokenizer",
            "--vocab-file", tokenizer_name,
            "--dataset-impl", "mmap",
            "--seq-length", str(seq_length),
            "--workers", str(workers),
        ]
        result = subprocess.run(cmd, capture_output=True, text=True)
        if result.returncode == 0:
            print(f"[data_prep] Tokenized {corpus_path} → {output_prefix}")
            return
        else:
            print(f"[data_prep] gpt-neox tokenizer failed: {result.stderr[:200]}")

    # Fallback: HuggingFace tokenizer → custom binary format
    _tokenize_hf_fallback(corpus_path, output_prefix, tokenizer_name, seq_length)


def _tokenize_hf_fallback(
    corpus_path: str,
    output_prefix: str,
    tokenizer_name: str,
    seq_length: int,
) -> None:
    """Fallback tokenizer writing gpt-neox-compatible mmap binary."""
    import numpy as np
    from transformers import AutoTokenizer

    print(f"[data_prep] Using HF fallback tokenizer for {corpus_path}")
    tokenizer = AutoTokenizer.from_pretrained(tokenizer_name)
    texts = load_corpus(corpus_path)

    all_ids = []
    for text in texts:
        ids = tokenizer.encode(text, add_special_tokens=True)
        # Chunk into seq_length
        for i in range(0, len(ids), seq_length):
            chunk = ids[i:i + seq_length]
            if len(chunk) == seq_length:
                all_ids.extend(chunk)

    arr = np.array(all_ids, dtype=np.uint16)
    bin_path = output_prefix + ".bin"
    idx_path = output_prefix + ".idx"
    arr.tofile(bin_path)
    # Write minimal index: num_docs, then offsets
    n_seqs = len(all_ids) // seq_length
    with open(idx_path, "wb") as f:
        f.write(np.array([n_seqs, seq_length], dtype=np.int64).tobytes())
    print(f"[data_prep] Written {n_seqs} sequences to {bin_path}")


def prepare_all_data(cfg: HM2Config, quick: bool = True) -> None:
    """Full data preparation pipeline for h-m2.

    1. Generate C7 from C3 (demographic shuffle)
    2. Tokenize all 8 corpora (C0-C7)
    3. Generate gpt-neox YAML configs
    """
    he1_dir = Path(cfg.he1_data_dir)
    tokenized_dir = Path(cfg.tokenized_dir)

    # Step 1: Generate C7 negative control
    c3_path = str(he1_dir / "C3.jsonl")
    c7_path = str(he1_dir / "C7.jsonl")
    if not Path(c7_path).exists():
        generate_c7_corpus(c3_path, c7_path, seed=cfg.seed)
    else:
        print(f"[data_prep] C7 already exists: {c7_path}")

    # Step 2: Tokenize all corpora
    for config_id in ALL_CONFIGS:
        corpus_path = str(he1_dir / f"{config_id}.jsonl")
        if not Path(corpus_path).exists():
            print(f"[data_prep] WARNING: {corpus_path} not found, skipping")
            continue
        out_dir = str(tokenized_dir / f"config_{config_id}")
        bin_path = Path(out_dir) / "text_document.bin"
        if bin_path.exists():
            print(f"[data_prep] Already tokenized: {config_id}")
            continue
        tokenize_corpus(corpus_path, out_dir)

    # Step 3: Build YAML configs
    build_all_yaml_configs(cfg, quick=quick)
    print("[data_prep] Data preparation complete.")


if __name__ == "__main__":
    import argparse
