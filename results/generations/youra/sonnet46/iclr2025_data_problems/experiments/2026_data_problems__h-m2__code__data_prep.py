"""H-M2 Data Preparation: tokenization pipeline and C7 negative control generation."""
import json
import random
import re
import subprocess
from pathlib import Path
from typing import List, Optional

from config import HM2Config, ALL_CONFIGS, load_config


DEMOGRAPHIC_TOKENS: List[str] = [
    "he", "she", "him", "her", "his", "hers",
    "man", "woman", "boy", "girl",
    "male", "female", "gentleman", "lady",
]

# Pattern to match demographic tokens as whole words
_DEMO_PATTERN = re.compile(
    r'\b(' + '|'.join(re.escape(t) for t in DEMOGRAPHIC_TOKENS) + r')\b',
    re.IGNORECASE
)


def load_corpus(corpus_path: str) -> List[str]:
    """Load JSONL corpus; each line is {text: ...} or a plain string."""
    texts = []
    with open(corpus_path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                obj = json.loads(line)
                texts.append(obj.get("text", obj.get("content", str(obj))))
            except json.JSONDecodeError:
                texts.append(line)
    return texts


def shuffle_demographic_tokens(text: str, rng: random.Random) -> str:
    """Globally permute all demographic tokens in text (C7 generation)."""
    matches = list(_DEMO_PATTERN.finditer(text))
    if not matches:
        return text
    tokens_found = [m.group() for m in matches]
    shuffled = tokens_found.copy()
    rng.shuffle(shuffled)
    result = list(text)
    for i, match in enumerate(reversed(matches)):
        start, end = match.start(), match.end()
        # Preserve original case pattern
        orig = text[start:end]
        replacement = shuffled[-(i + 1)]
        if orig[0].isupper():
            replacement = replacement.capitalize()
        result[start:end] = list(replacement)
    return "".join(result)


def generate_c7_corpus(
    c3_path: str,
    output_path: str,
    seed: int = 42,
) -> None:
    """Generate C7: C3 corpus with demographic tokens globally permuted.

    Args:
        c3_path: Path to C3.jsonl corpus
        output_path: Path to write C7.jsonl
        seed: Random seed for reproducibility
    """
    rng = random.Random(seed)
    texts = load_corpus(c3_path)
    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as f:
        for text in texts:
            shuffled = shuffle_demographic_tokens(text, rng)
            f.write(json.dumps({"text": shuffled}) + "\n")
    print(f"[data_prep] C7 corpus written: {len(texts)} docs → {output_path}")


