FEASIBILITY_CONSTRAINTS = """\

---

## MANDATORY FEASIBILITY CONSTRAINTS (Pipeline-Enforced)

Reject ideas that require **new benchmarks, rubrics, or scoring frameworks**.
Reject ideas that require **synthetic/generated data or future follow-up data that does not yet exist**.
Reject ideas that require **human evaluation, annotation, or subjective scoring by human raters**.
Accept only hypotheses that can be **tested immediately using existing real datasets and existing benchmarks**."""


def build_phase0_prompt(research_idea_content: str, input_file_path: str = None, research_folder: str = None) -> str:
    """
    Build the initial prompt for Phase 0 unattended execution.

    The prompt instructs Claude to:
    1. Execute /phase0-brainstorm workflow
    2. Use the provided research idea content as research_idea_content
    3. Run in UNATTENDED mode (auto-fill, no user interaction)
    4. Save output to research_folder if specified
    """
    file_ref = f"\nInput file: {input_file_path}" if input_file_path else ""
    folder_ref = f"\nOutput research folder: {research_folder}\nSave all Phase 0 outputs (00_brainstorm_session.md) to this folder." if research_folder else ""

    prompt = f"""/phase0-brainstorm

#batch-mode

Proceed in Unattended mode. Below is the research_idea_content to use for auto-fill.
Do NOT ask for user input — extract all research components from this content and complete Phase 0 automatically.
{file_ref}{folder_ref}

---

{research_idea_content}{FEASIBILITY_CONSTRAINTS}"""

    return prompt


# ============================================================
# Signal Handlers
# ============================================================
_claude_process = None


def signal_handler(signum, frame):
    """Handle SIGINT/SIGTERM gracefully."""
    sig_name = signal.Signals(signum).name
    log(f"Received {sig_name} — shutting down")

    if _claude_process and _claude_process.poll() is None:
        log("Terminating Claude CLI process...")
        _claude_process.terminate()
        try:
            _claude_process.wait(timeout=10)
        except subprocess.TimeoutExpired:
            log("Force killing Claude CLI process...")
            _claude_process.kill()

    cleanup()
    sys.exit(128 + signum)


