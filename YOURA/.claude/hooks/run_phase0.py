def parse_args():
    """Parse CLI arguments."""
    parser = argparse.ArgumentParser(
        description="Phase 0 Launcher — Run /phase0-brainstorm unattended",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  %(prog)s docs/research_idea.md
  %(prog)s "Weak supervision for image classification"
  %(prog)s docs/research_idea.md --timeout 1800
        """,
    )

    parser.add_argument("input", type=str,
                        help="Research idea .md file path OR topic string")
    parser.add_argument("--research-folder", type=str, default=None,
                        help="Output research folder path (default: auto-create new timestamped folder)")
    parser.add_argument("--timeout", type=int, default=3600,
                        help="Max runtime in seconds (default: 3600)")
    parser.add_argument("--retry-prompt", type=str, default=None,
                        help="Override prompt for retry run (injected by run_early_pipeline.py)")

    return parser.parse_args()



def _check_passed(phase: str) -> bool:
    """Read last verify JSON and return passed flag."""
    import json as _json
    p = CACHE_DIR / f"{phase}_output_verify.json"
    if not p.exists():
        return False
    try:
        return _json.load(open(p)).get("passed", False)
    except Exception:
        return False


def _set_proc(p):
    global _claude_process
    _claude_process = p


def _run_claude(prompt: str, timeout: int, output_log) -> int:
    """Launch Claude CLI with retry on transient API overload (5xx/Overloaded).

    Delegates to claude_runner.run_claude_with_retry.
    """
    from claude_runner import run_claude_with_retry
    return run_claude_with_retry(
        claude_cli=CLAUDE_CLI,
        project_dir=PROJECT_DIR,
        cache_dir=CACHE_DIR,
        prompt=prompt,
        timeout=timeout,
        output_log=output_log,
        phase_name="phase0",
        log_fn=log,
        process_setter=_set_proc,
        echo_to_stdout=True,
        monitor_complete_lock=False,
    )


def main():
    global _claude_process

    # Ensure .serena/ exists so this project gets its own empty memory store
    ensure_serena_initialized()

    args = parse_args()

    # Resolve input (file or topic string)
    research_idea_content, topic_summary, input_file_path = resolve_input(args.input)

    # Resolve research folder (optional — if not given, Claude will create a new one)
    research_folder = None
    if args.research_folder:
        research_folder = str(Path(args.research_folder).resolve())
        os.makedirs(research_folder, exist_ok=True)

    CACHE_DIR.mkdir(parents=True, exist_ok=True)
    clear_must_stop()  # Clear any stale MUST_STOP flag from previous run
    clear_phase_complete_lock("phase0")  # Clear any stale PHASE_COMPLETE lock from previous run

    log("=" * 60)
    log("Phase 0 Launcher starting")
    log(f"  Input: {args.input}")
    if input_file_path:
        log(f"  File: {input_file_path}")
    log(f"  Topic: {topic_summary}")
    log(f"  Research folder: {research_folder or 'AUTO (new timestamped folder)'}")
    log(f"  Content length: {len(research_idea_content)} chars")
    log(f"  Timeout: {args.timeout}s")

    # Register cleanup
    atexit.register(cleanup)
    signal.signal(signal.SIGINT, signal_handler)
    signal.signal(signal.SIGTERM, signal_handler)

    # Step 1: Inject research topic into config (for LLM orchestrator context)
    inject_research_inputs(topic_summary)

    # Step 2: Enable auto_responder_config
    set_auto_responder_enabled(True)

    # Step 3: Create active_phase.json (include research_folder for snapshot-based diff)
    create_active_phase(research_folder=research_folder)

    # Step 4: Build prompt and launch Claude CLI
    prompt = build_phase0_prompt(research_idea_content, input_file_path, research_folder)

    log(f"Launching Claude CLI with prompt ({len(prompt)} chars)")
    log(f"CWD: {PROJECT_DIR}")

    output_log = CACHE_DIR / "phase0_claude_output.log"
    output_log.write_text("", encoding="utf-8")

    start_time = time.time()
    try:
        from claude_runner import run_claude_with_retry
        exit_code = run_claude_with_retry(
            claude_cli=CLAUDE_CLI,
            project_dir=PROJECT_DIR,
            cache_dir=CACHE_DIR,
            prompt=prompt,
            timeout=args.timeout,
            output_log=output_log,
            phase_name="phase0",
            log_fn=log,
            process_setter=_set_proc,
            echo_to_stdout=False,
            monitor_complete_lock=False,
            extra_cli_args=["--output-format", "stream-json", "--verbose"],
            stream_json=True,
            separate_stderr=True,
        )
    except FileNotFoundError:
        log(f"ERROR: Claude CLI not found at {CLAUDE_CLI}")
        cleanup()
        sys.exit(1)
    except Exception as e:
        log(f"ERROR: {e}")
        cleanup()
        sys.exit(1)

    elapsed = time.time() - start_time
    log(f"Claude CLI exited with code {exit_code} after {elapsed:.0f}s")
    main_timed_out = (exit_code == 124)
    if main_timed_out:
        log_timeout_marker(log, "phase0", elapsed, attempt="initial")

    if main_timed_out:
        cleanup()
        rc = post_timeout_exit_code(research_folder, "phase0", log)
        sys.exit(rc)

    # Check for MUST_STOP signal from hook (fatal error — skip verify/retry)
    must_stop_reason = check_must_stop()
    if must_stop_reason:
        log(f"MUST_STOP detected — aborting phase0: {must_stop_reason}")
        cleanup()
        sys.exit(1)

    # Verify outputs; retry with corrective prompt if needed
    verify_and_write_json("phase0", research_folder, exit_code)
    clear_phase_complete_lock("phase0")  # Clear lock so retry session is not immediately approved
    for _attempt in range(MAX_RETRIES):
        if exit_code != 0 or _check_passed("phase0"):
            break
        log(f"Phase 0 verification FAILED — retry {_attempt + 1}/{MAX_RETRIES}")
        _retry_prompt = build_retry_prompt("phase0", research_folder)
        exit_code = _run_claude(_retry_prompt, args.timeout,
                               CACHE_DIR / "phase0_claude_output.log")
        if exit_code == 124:
            cleanup()
            rc = post_timeout_exit_code(research_folder, "phase0", log)
            sys.exit(rc)
        verify_and_write_json("phase0", research_folder, exit_code)

    # Step 5: Cleanup
    cleanup()

    log("Phase 0 Launcher complete")
    log("=" * 60)
    sys.exit(exit_code)


if __name__ == "__main__":
    main()
