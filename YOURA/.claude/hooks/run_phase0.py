def ensure_serena_initialized():
    """Create .serena/ with empty memories if it doesn't exist yet.

    This prevents Phase 0 from inheriting memories from a different
    Serena project that happens to share the same project_name.
    """
    if SERENA_PROJECT_YML.exists():
        return  # already initialized

    project_name = PROJECT_DIR.name
    SERENA_DIR.mkdir(parents=True, exist_ok=True)
    SERENA_MEMORIES_DIR.mkdir(exist_ok=True)

    content = SERENA_PROJECT_TEMPLATE.format(project_name=project_name)
    SERENA_PROJECT_YML.write_text(content, encoding="utf-8")

    # stderr log (log() may not be ready yet)
    print(f"[run_phase0] Initialized .serena/ for project '{project_name}' "
          f"at {SERENA_DIR}", file=sys.stderr)


def log(message: str):
    """Log to file and stderr."""
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    entry = f"[{timestamp}] {message}"
    print(entry, file=sys.stderr)
    try:
        with open(LOG_FILE, "a", encoding="utf-8") as f:
            f.write(entry + "\n")
    except Exception:
        pass


