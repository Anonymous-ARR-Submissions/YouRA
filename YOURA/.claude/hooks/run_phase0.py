# Input Handling
# ============================================================
def resolve_input(input_arg: str) -> tuple:
    """
    Resolve input argument — either a .md file path or a plain text topic.

    Returns:
        (research_idea_content: str, topic_summary: str, input_file_path: str or None)
    """
    input_path = Path(input_arg)

    # Check if it's a file path (absolute or relative)
    if input_path.exists() and input_path.is_file():
        log(f"Input is a file: {input_path}")
        with open(input_path, "r", encoding="utf-8") as f:
            content = f.read()
        # Extract first line as topic summary (skip markdown headers)
        lines = [l.strip() for l in content.strip().split("\n") if l.strip() and not l.strip().startswith("#")]
        topic_summary = lines[0][:100] if lines else input_path.stem
        return content, topic_summary, str(input_path.resolve())

    # Also check relative to PROJECT_DIR
    project_path = PROJECT_DIR / input_arg
    if project_path.exists() and project_path.is_file():
        log(f"Input is a file (relative to project): {project_path}")
        with open(project_path, "r", encoding="utf-8") as f:
            content = f.read()
        lines = [l.strip() for l in content.strip().split("\n") if l.strip() and not l.strip().startswith("#")]
        topic_summary = lines[0][:100] if lines else project_path.stem
        return content, topic_summary, str(project_path.resolve())

    # It's a plain text topic string
    log(f"Input is a topic string: {input_arg}")
    return input_arg, input_arg, None


